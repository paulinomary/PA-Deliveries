function addgridtofigure(s,g)

line([0.5:g(1):s(2)+0.5;0.5:g(1):s(2)+0.5], ...
        [0.5*ones(1,s(2)/g(2)+1);(s(1)+0.5)* ...
        ones(1,s(2)/g(2)+1)],'Color','w');
    line([0.5*ones(1,s(1)/g(1)+1);(s(2)+0.5)* ...
        ones(1,s(1)/g(2)+1)], ...
        [0.5:g(2):s(1)+0.5;0.5:g(2):s(1)+0.5], ...
        'Color','w');
